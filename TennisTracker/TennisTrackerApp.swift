import SwiftUI
import SwiftData
import UserNotifications
import AppIntents

@main
struct TennisTrackerApp: App {
    var sharedModelContainer: ModelContainer = {
        let schema = Schema([
            Player.self,
            Match.self,
            Point.self
        ])
        let modelConfiguration = ModelConfiguration(schema: schema, isStoredInMemoryOnly: false)

        do {
            return try ModelContainer(for: schema, configurations: [modelConfiguration])
        } catch {
            fatalError("Could not create ModelContainer: \(error)")
        }
    }()

    var body: some Scene {
        WindowGroup {
            ContentView()
                .onAppear {
                    requestNotificationPermissions()
                    checkPendingActions()
                    setupDarwinNotificationListener()
                }
                .onOpenURL { url in
                    handleURL(url)
                }
        }
        .modelContainer(sharedModelContainer)
    }

    private func requestNotificationPermissions() {
        // Set delegate to show notifications even when app is in foreground
        UNUserNotificationCenter.current().delegate = NotificationDelegate.shared

        UNUserNotificationCenter.current().requestAuthorization(options: [.alert, .sound, .badge]) { granted, error in
            DispatchQueue.main.async {
                if granted {
                    print("üéæ Notification permissions granted - Live Activities should work")
                } else {
                    print("‚ùå Notification permissions denied - Live Activities won't work")
                    if let error = error {
                        print("‚ùå Permission error: \(error)")
                    }
                }
            }
        }
    }

    // Handle URL schemes from Live Activity buttons
    private func handleURL(_ url: URL) {
        let timestamp = Date()
        print("üîó URL RECEIVED [\(timestamp)]: \(url)")

        guard url.scheme == "tennistracker" else {
            print("‚ùå Unknown URL scheme: \(url.scheme ?? "nil")")
            return
        }

        print("üîó Processing tennistracker URL with host: '\(url.host ?? "nil")'")

        // Check if this is a silent action (from Live Activity)
        let urlComponents = URLComponents(url: url, resolvingAgainstBaseURL: false)
        let isSilent = urlComponents?.queryItems?.contains { $0.name == "silent" && $0.value == "true" } ?? false

        if isSilent {
            print("üîá SILENT ACTION: Processing Live Activity button press without UI focus")
        }

        switch url.host {
        case "winner":
            print("üèÜ Winner button pressed from Live Activity")
            print("üèÜ Posting NotificationCenter notification for winner...")
            NotificationCenter.default.post(
                name: NSNotification.Name("RecordPointFromWidget"),
                object: nil,
                userInfo: ["pointType": "winner", "player": "Mark"]
            )
            print("üèÜ ‚úÖ Winner notification posted successfully")
        case "error":
            print("üôà Error button pressed from Live Activity")
            print("üôà Posting NotificationCenter notification for error...")
            NotificationCenter.default.post(
                name: NSNotification.Name("RecordPointFromWidget"),
                object: nil,
                userInfo: ["pointType": "unforcedError", "player": "Mark"]
            )
            print("üôà ‚úÖ Error notification posted successfully")
        default:
            print("‚ùì Unknown URL action: '\(url.host ?? "nil")'")
        }

        // For silent actions, send the app to background after a brief delay
        if isSilent {
            print("üîá SILENT: Scheduling app backgrounding...")
            DispatchQueue.main.asyncAfter(deadline: .now() + 0.5) {
                print("üîá SILENT: Sending app to background...")
                // Note: In iOS, apps can't force themselves to background
                // But we can minimize UI disruption by not showing alerts or navigation
            }
        }

        print("üîó URL handling completed [\(timestamp)]")
    }

    // Set up listener for widget extension Darwin notifications
    private func setupDarwinNotificationListener() {
        print("üì° Setting up Darwin notification listener for widget actions...")

        // Use a simpler approach - just register for the notification
        let center = CFNotificationCenterGetDarwinNotifyCenter()

        CFNotificationCenterAddObserver(
            center,
            nil,
            { _, _, name, _, _ in
                DispatchQueue.main.async {
                    // Post a local notification that our app can receive
                    NotificationCenter.default.post(
                        name: NSNotification.Name("WidgetActionReceived"),
                        object: nil
                    )
                }
            },
            "com.tennis.tracker.widgetAction" as CFString,
            nil,
            .deliverImmediately
        )

        // Set up local notification observer
        NotificationCenter.default.addObserver(
            forName: NSNotification.Name("WidgetActionReceived"),
            object: nil,
            queue: .main
        ) { _ in
            self.handleWidgetNotification()
        }

        print("üì° ‚úÖ Darwin notification listener set up successfully")
    }

    // Handle notifications from widget extension
    private func handleWidgetNotification() {
        print("üì° RECEIVED DARWIN NOTIFICATION from widget extension")
        checkPendingActions() // Process any pending actions
    }

    // Check for pending actions from Live Activity buttons
    private func checkPendingActions() {
        let timestamp = Date()
        print("üîç CHECKING FOR PENDING ACTIONS [\(timestamp)]...")

        // TEST MECHANISM: Simulate a widget action for testing (remove this later)
        // Uncomment the lines below to test the notification flow without widget
        /*
        print("üß™ TEST: Simulating widget action for testing...")
        NotificationCenter.default.post(
            name: NSNotification.Name("RecordPointFromWidget"),
            object: nil,
            userInfo: ["pointType": "winner", "player": "Mark"]
        )
        print("üß™ TEST: Widget action simulation completed")
        */

        let defaults = UserDefaults(suiteName: "group.com.markmoriarty.apps.TennisTracker")
        print("üîç UserDefaults created: \(defaults != nil ? "YES" : "NO")")

        // Always log all available keys for debugging
        if let allKeys = defaults?.dictionaryRepresentation().keys {
            let keyArray = Array(allKeys).sorted()
            print("üîç ALL UserDefaults keys (\(keyArray.count)): \(keyArray)")
        } else {
            print("üîç No UserDefaults keys found")
        }

        // Check specifically for pendingAction
        let pendingAction = defaults?.dictionary(forKey: "pendingAction")
        print("üîç Direct pendingAction check: \(pendingAction != nil ? "FOUND" : "NOT FOUND")")

        if let actionData = pendingAction {
            print("üéØ PENDING ACTION DETECTED: \(actionData)")

            // Extract timestamp for tracking
            if let timestamp = actionData["timestamp"] as? Double {
                let actionDate = Date(timeIntervalSince1970: timestamp)
                let timeDiff = Date().timeIntervalSince(actionDate)
                print("üéØ Action timestamp: \(actionDate), age: \(timeDiff)s")
            }

            // Clear the action so it only processes once
            defaults?.removeObject(forKey: "pendingAction")
            defaults?.synchronize()
            print("üéØ Action cleared from UserDefaults")

            // Process the action
            if let action = actionData["action"] as? String,
               let pointType = actionData["pointType"] as? String,
               let player = actionData["player"] as? String,
               action == "recordPoint" {

                print("üéØ PROCESSING: \(pointType) for \(player)")

                // Post notification so PlayView can handle it
                print("üéØ Posting NotificationCenter notification...")
                NotificationCenter.default.post(
                    name: NSNotification.Name("RecordPointFromWidget"),
                    object: nil,
                    userInfo: ["pointType": pointType, "player": player]
                )
                print("üéØ ‚úÖ Notification posted successfully")
            } else {
                print("‚ùå Failed to parse action data properly")
                print("‚ùå action='\(actionData["action"] ?? "nil")'")
                print("‚ùå pointType='\(actionData["pointType"] ?? "nil")'")
                print("‚ùå player='\(actionData["player"] ?? "nil")'")
            }
        } else {
            print("üîç No pending actions found")
        }

        print("üîç checkPendingActions completed [\(timestamp)]")
    }

    // Send a local notification
    func sendGameNotification(title: String, body: String) {
        let content = UNMutableNotificationContent()
        content.title = title
        content.body = body
        content.sound = .default

        let request = UNNotificationRequest(identifier: UUID().uuidString, content: content, trigger: nil)
        UNUserNotificationCenter.current().add(request) { error in
            if let error = error {
                print("‚ùå Failed to send notification: \(error)")
            } else {
                print("üîî Notification sent: \(title)")
            }
        }
    }
}

// Notification delegate to show notifications even when app is in foreground
class NotificationDelegate: NSObject, UNUserNotificationCenterDelegate {
    static let shared = NotificationDelegate()

    func userNotificationCenter(_ center: UNUserNotificationCenter, willPresent notification: UNNotification, withCompletionHandler completionHandler: @escaping (UNNotificationPresentationOptions) -> Void) {
        // Show notifications even when app is in foreground
        completionHandler([.banner, .sound, .badge])
    }
}